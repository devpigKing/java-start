package variable;

public class Var5 {
    public static void main(String[] args) {

        // 1. 변수 선언, 초기화 따라
        int A;
        A = 10;
        System.out.println(A);

        // 2. 변수 선언 및 초기화를 한번에
        int B = 15;
        System.out.println(B);

        // 3. 여러 변수 선언 및 초기화 한번에
        int c = 3, d = 5;
        System.out.println(c);
        System.out.println(d);

    }


}
